            Logger.getLogger(ActualizarCliente.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Hubo un error al actualizar el cliente", "Erorr!!", JOptionPane.INFORMATION_MESSAGE);
